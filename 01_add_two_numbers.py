def main ():
    
    # Get user input
    num1 = (input("Enter first number: "))
    num1 = int(num1)  # Convert input to integer

    num2 = int(input("Enter second number: "))
    num2 = int(num2)  # Convert input to integer

    # Add the two numbers
    result = num1 + num2

    # Display the result
    print(f"The sum of {num1} + {num2} = {result}")

if __name__ == "__main__":
    main()

